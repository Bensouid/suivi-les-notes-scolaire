    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
 int row = tableEtudiant.getSelectedRow();
if (row == -1) {
        JOptionPane.showMessageDialog(this, "Sélectionnez un étudiant!");
        return;
    }
    
    int confirm = JOptionPane.showConfirmDialog(
        this, 
        "Voulez-vous vraiment supprimer?", 
        "Confirmation", 
        JOptionPane.YES_NO_OPTION
    );
    
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            int id = Integer.parseInt(tableEtudiant.getValueAt(row, 0).toString());
            
            EtudiantDAO dao = new EtudiantDAO();
            dao.supprimer(id);
            
            chargerEtudiants();
            txtnom.setText("");
            txtprenom.setText("");
            
            JOptionPane.showMessageDialog(this, "Supprimé avec succès!");
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erreur: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_btnSupprimerActionPerformed

