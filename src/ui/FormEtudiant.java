private void chargerEtudiants() {
    if (tableEtudiant == null) {
        return;
    }

    DefaultTableModel model = (DefaultTableModel) tableEtudiant.getModel();
    model.setRowCount(0);

    EtudiantDAO dao = new EtudiantDAO();
    List<Etudiant> list = dao.getAllEtudiants();

    if (list == null) return;

    for (Etudiant e : list) {
        model.addRow(new Object[]{
            e.getId(),
            e.getNom(),
            e.getPrenom()
        });
    }
    sorter = new TableRowSorter<>((DefaultTableModel) tableEtudiant.getModel());
tableEtudiant.setRowSorter(sorter);

}

