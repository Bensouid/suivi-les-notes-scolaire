    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO 
        // جلب النص من الحقول
String nom = txtnom.getText();
String prenom = txtprenom.getText();

// التحقق من الحقول
if (nom.isEmpty() || prenom.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Remplir tous les champs !");
    return;
}

// إنشاء كائن Etudiant
Etudiant e = new Etudiant();
e.setNom(nom);
e.setPrenom(prenom);

// إضافة للقاعدة
EtudiantDAO dao = new EtudiantDAO();
dao.ajouterEtudiant(e);

// رسالة نجاح
JOptionPane.showMessageDialog(this, "Etudiant ajouté ✅");

// تفريغ الحقول
txtnom.setText("");
txtprenom.setText("");

// تحديث الجدول تلقائياً
List<Etudiant> list = dao.getAllEtudiants();

DefaultTableModel model = new DefaultTableModel();
model.addColumn("ID");
model.addColumn("Nom");
model.addColumn("Prenom");

for (Etudiant etud : list) {
    model.addRow(new Object[]{
        etud.getId(),
        etud.getNom(),
        etud.getPrenom()
    });
}

tableEtudiant.setModel(model);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void tableEtudiantMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEtudiantMouseClicked

    int row = tableEtudiant.getSelectedRow();

  if (row != -1) {
        try {
            // Get ID
            int id = Integer.parseInt(tableEtudiant.getValueAt(row, 0).toString());
            
            // Afficher Nom o Prenom
            txtnom.setText(tableEtudiant.getValueAt(row, 1).toString());
            txtprenom.setText(tableEtudiant.getValueAt(row, 2).toString());
            
            // Calcul Moyenne - JDID!
            NoteDAO noteDAO = new NoteDAO();
            double moyenne = noteDAO.calculerMoyenne(id);
            txtMoyenne.setText(String.format("%.2f", moyenne));
            
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erreur: " + ex.getMessage());
        }
    }

    }//GEN-LAST:event_tableEtudiantMouseClicked

