package utils;

import java.sql.Connection;
import java.sql.DriverManager;

public class ConnexionDB {

    public static Connection getConnection() {
        try {
            return DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/gestion_notes",
                "root",
                ""
            );
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }
}
